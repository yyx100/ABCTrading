package com.agilebc.data.trade;

public class Exchange {

	private int exchgId = 0;
	private String exchgCode = null;
	private String exchgFullName = null;
	
	
	
	
	public int getExchgId() {
		return exchgId;
	}
	public void setExchgId(int exchgId) {
		this.exchgId = exchgId;
	}
	public String getExchgCode() {
		return exchgCode;
	}
	public void setExchgCode(String exchgCode) {
		this.exchgCode = exchgCode;
	}
	public String getExchgFullName() {
		return exchgFullName;
	}
	public void setExchgFullName(String exchgFullName) {
		this.exchgFullName = exchgFullName;
	}
	
	
	
}
